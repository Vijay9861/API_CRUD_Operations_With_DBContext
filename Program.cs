
using Microsoft.EntityFrameworkCore;
using NewAPI.Models;

namespace NewAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();
            builder.Services.AddDbContext<UsbmContext>
            (options => options.UseSqlServer(builder.Configuration.GetConnectionString("Data Source=DESKTOP-AU94EJS;Initial Catalog=Usbm;Integrated Security=True;TrustServerCertificate=True;Trusted_Connection=True")));


            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
